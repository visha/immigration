          <h2><span>Immigration Process</span></h2>
          <div class="clr"></div>
          <p>The following are the steps that in most cases will be followed from a client’s initial consultation, through retainer and ultimately conclusion of the application process:</p>
          <p>Initial consultation.  During this meeting Hart A. Kaminker or his representative will gather the necessary information in order to make a determination as to what will be the best process for the client to follow in order to obtain residence in Canada.  During this initial consultation Hart A. Kaminker will also set out the prospects of success of a proposed application or even if an application is viable. </p> 
		  <p>Hart A. Kaminker will advise the client of all documentation that will be required in order to support the client’s application for residence either permanent or temporary.  In addition Hart A. Kaminker will assist the client in filling out the required application forms. </p>
		  <p>Once all of the supporting documentation and application forms are completed Hart A. Kaminker will prepare an application package to be forwarded to the relevant Visa Post which will include the application forms, supporting documents, and a covering supporting submission letter from Hart A. Kaminker.</p>
		  <p>Hart A. Kaminker will guide the client through the entire application process including assisting the client in responding to any inquiries from the Visa Post.</p>
		  <p>Once the client is determined to have met the requirements of the program under which the client has applied, and has met all other statutory requirements including medical, criminal and security clearance the Visa Post will issue the document applied for.</p>
		  <p>Hart A. Kaminker’s office will assist the client with settlement in Canada including guidance with respect to applying for health coverage, social insurance number, banking, job search if required among other settlement matters.  Some of these services will be offered by outside professionals and those outside professionals may charge a fee for those services.</p>
