          <h2><span>Skilled Worker</span></h2>
          <div class="clr"></div>
          
          <img src="images/img_1.jpg" width="613" height="179" alt="image" />
          <div class="clr"></div>
          <p><strong>Skilled Workers  are selected as permanent residents based on their education, work experience, knowledge of English and / or French, and other criteria that have been shown to help them become economically established in Canada.</strong></p>
          <p>The rules for applying as a federal skilled worker can change from time to time without notice, so make sure you are in touch with the office of Global Enterprises regularly if you are considering immigrating to Canada under this category.</p>	  
          <p>Global Enterprises can provide you good guidance and knowledge based counseling service so that your aspiration to migrate to Canada under Skilled Worker category is not shattered.</p>
		  <p><h3>Who can apply</h3></p>
		  <p>Skilled workers are people who are selected as permanent residents based on their ability to become economically established in Canada</p>
		  <p>Federal skilled worker applications are assessed for eligibility according to the criteria set out below.</p>
		  <p><h3>For your application to be eligible for processing, you must:</h3>
		   <ul>
		    <li>Include the results of your official language proficiency test, AND</li>
			<li>Have a valid offer of arranged employment, OR</li>
			<li>Have one year of continuous full-time paid work experience in at least one of the occupations listed by the Canadian Government. </li>
		    </ul></p>
		  <p><h3>Work experience minimum requirements</h3></p>
		  <p>If your application is eligible for processing, it will then be assessed against minimum requirements.</p>
          <p><h3>Your work experience must be:</h3></p>
         <ul>
		    <li>For at least one year, continuous, and paid (full-time or the equivalent in part-time), AND</li>
			<li>Skill Type 0 (managerial occupations) or Skill Level A (professional occupations) or B (technical occupations and skilled trades) on the Canadian National Occupational Classification (NOC) list, AND</li>
			<li>Within the last 10 years.</li>
		    </ul>
			<p>If you meet the above minimum requirements, your application will be processed according to the six selection factors in the skilled worker points grid, which are:</p>
			 <ul>
		    <li>Your education</li>
			<li>Your abilities in English and/or French, Canada's two official languages</li>
			<li>Your work experience</li>
			<li>Your age</li>
			<li>Whether you have arranged employment in Canada, and</li>
			<li>Your adaptability</li>
			</ul>
		    <p>You must also show that you have enough money to support yourself and your dependants after you arrive in Canada.</p>
      
